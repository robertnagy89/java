public class Atvalto {
    public static double meter_lab(int meter) {
        return 3.2808398950131 * meter;
    }

    public static double lab_meter(int lab) {
        return 0.3048 * lab;
    }

    public static double kilometer_merfold(int kilometer) {
        return 0.62 * kilometer;
    }

    public static double merfold_kilometer(int merfold) {
        return 1.61 * merfold;
    }
}
